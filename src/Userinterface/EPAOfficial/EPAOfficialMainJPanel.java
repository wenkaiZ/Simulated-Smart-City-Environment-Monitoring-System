    public void populateReportTable(){
        DefaultTableModel model = (DefaultTableModel) reportJTable.getModel();
        model.setRowCount(0);
        for(Network n : ecoSystem.getNetworkList()){
            //在同一城市
            if(n.getName().equals(enterprise.getUserAccountDirectory().getUserAccountList().get(0).getArea())){
                for(Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()){
                    //是社区类型
                    if(e.getEnterpriseType() == Enterprise.EnterpriseType.Community){
                        //在同一个地区，因为一个城市有很多地区，所以需要找到是相同地区的
                        if(e.getName().equals(userAccount.getArea())){
                            communityOfficialAccount = e.getUserAccountDirectory().getUserAccountList().get(0);
                            for(PostMessage p : communityOfficialAccount.getMessageQueue().getMessages()){
                                //接受人是这个官员
                                if(p.getReceiver() == userAccount){
                                    Object[] row = new Object[3];
                                    row[0] = communityOfficialAccount;
                                    row[1] = ((PostMessage)p).getMessage();
                                    row[2] = ((PostMessage)p).getPostDate();

                                    model.addRow(row);
                                }
                            }
                        }
                    }
                }
            }
        }
    }
    
    public void populateMessageTable(){
        DefaultTableModel model = (DefaultTableModel) analystMessageJTable.getModel();
        model.setRowCount(0);
        for(Organization o : enterprise.getOrganizationDirectory().getOrganizationList()){
            //在同一个enterprise下找到EPAAnalyst类型的orga
            if(o.getOrganizationType() == Organization.Type.EPAAnalyst){
                for(PostMessage p : o.getMessageQueue().getMessages()){
                    //在EPAAnalyst的organization消息列表中找到属于自己的消息
                    if(p.getReceiver() == userAccount){
                        Object[] row = new Object[2];
                        row[0] = ((PostMessage)p).getMessage();
                        row[1] = ((PostMessage)p).getPostDate();

                        model.addRow(row);
                    }
                }
            }
        }
    }
