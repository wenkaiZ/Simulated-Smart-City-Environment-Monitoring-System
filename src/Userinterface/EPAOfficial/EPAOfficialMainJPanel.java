    private void sendWarnJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendWarnJButtonActionPerformed
        // TODO add your handling code here:
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to SEND WARN REQUEST?", "Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                PostMessage newMessage = new NeedConfirmMessage();
                newMessage.setMessage("Please warn all of your community residents!");
                newMessage.setPostDate(new Date());
                
                newMessage.setReceiver(communityOfficialAccount);
                newMessage.setSender(userAccount);
                
                organization.getMessageQueue().getMessages().add(newMessage);
            }
    }//GEN-LAST:event_sendWarnJButtonActionPerformed

    private void sendUpdateCriteriaJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendUpdateCriteriaJButtonActionPerformed
        // TODO add your handling code here:
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to SEND UPDATE CRITERIA REQUEST?", "Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                PostMessage newMessage = new NeedConfirmMessage();
                newMessage.setMessage("Please update the criteria according to the new data!");
                newMessage.setPostDate(new Date());
                UserAccount ua = null;
                for(Organization o : enterprise.getOrganizationDirectory().getOrganizationList()){
                    //在同一个enterprise下找到EPAAnalyst类型的orga
                    if(o.getOrganizationType() == Organization.Type.EPAAnalyst){
                        for(UserAccount u : o.getUserAccountDirectory().getUserAccountList()){
                            //在EPAAnalyst的organization中找到一个地区的
                            if(u.getArea().equals(userAccount.getArea())){
                                ua = u;
                            }
                        }
                    }
                }
                newMessage.setReceiver(ua);
                newMessage.setSender(userAccount);
                
                organization.getMessageQueue().getMessages().add(newMessage);
            }
    }//GEN-LAST:event_sendUpdateCriteriaJButtonActionPerformed

