    private void populateCombo(){
        organizationTypeJComboBox.removeAllItems();
        for (Organization.Type type : Organization.Type.values()){
            if (type.getValue().equals(Organization.Type.Sensor.getValue())||type.getValue().equals(Organization.Type.SensorMaintenancer.getValue()))
                organizationTypeJComboBox.addItem(type);
        }
    }

    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        
        model.setRowCount(0);
        
        for (Organization organization : organizationDirectory.getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization.getName();
            row[1] = organization.getOrganizationType();
            
            model.addRow(row);
        }
    }
