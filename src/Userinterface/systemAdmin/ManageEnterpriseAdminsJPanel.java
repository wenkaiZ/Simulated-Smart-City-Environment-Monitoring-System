    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterprisesJTable.getModel();

        model.setRowCount(0);
        for (Network network : ecoSystem.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (UserAccount userAccount : enterprise.getUserAccountDirectory().getUserAccountList()) {
                    Object[] row = new Object[4];
                    row[0] = network.getName();
                    row[1] = enterprise.getEnterpriseType().getValue();
                    row[2] = enterprise.getName();
                    row[3] = userAccount.getUserName();

                    model.addRow(row);
                }
            }
        }
    }
    
    private void populateNetworkComboBox(){
        networkJComboBox.removeAllItems();
        
        for (Network network : ecoSystem.getNetworkList()){
            networkJComboBox.addItem(network);
        }
    }
    
    private void populateEnterpriseComboBox(Network network){
        enterpriseNameJComboBox.removeAllItems();
        
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
            enterpriseNameJComboBox.addItem(enterprise);
        }
        
    }
    
    private void populateEnterpriseAreaComBox(Network network){
        enterpriseAreaJComboBox.removeAllItems();
        enterpriseAreaJComboBox.addItem(network.getName());
        
    }

