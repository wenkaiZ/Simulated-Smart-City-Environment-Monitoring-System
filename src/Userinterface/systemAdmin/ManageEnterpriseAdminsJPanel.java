    private void createJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createJButtonActionPerformed
        // TODO add your handling code here:
        Enterprise enterprise = (Enterprise) enterpriseNameJComboBox.getSelectedItem();
        
        String username = usernameTxt.getText();
        String password = String.valueOf(jPasswordField.getPassword());
        UserAccount account = null;
        if(!enterprise.getUserAccountDirectory().checkIfUsernameIsUnique(username)){
            JOptionPane.showMessageDialog(null, "The user name has already existed.");
            return;
        }
        if(password.equals(String.valueOf(reJPasswordField.getPassword()))){
            if(enterprise.getEnterpriseType().getValue().equals("EPA")){
                //市长就是EPA的管理者，整个EPA Enterprise的头
                account = enterprise.getUserAccountDirectory().createUserAccount(username, password, new MayorRole(), enterprise.getName());
                account.setArea(String.valueOf(enterpriseAreaJComboBox.getSelectedItem()));
            }
            else if(enterprise.getEnterpriseType().getValue().equals("Community")){
                //社区官员是社区的管理者。
                account = enterprise.getUserAccountDirectory().createUserAccount(username, password, new CommunityOfficialRole(), enterprise.getName());
                account.setArea(String.valueOf(enterpriseAreaJComboBox.getSelectedItem()));
            }
            else if(enterprise.getEnterpriseType().getValue().equals("Sensor Company")){
                //传感器管理员就是公司的头。
                account = enterprise.getUserAccountDirectory().createUserAccount(username, password, new SensorManagerRole(), enterprise.getName());
                account.setArea(String.valueOf(enterpriseAreaJComboBox.getSelectedItem()));
            }
            populateTable();
        }
        else{
            JOptionPane.showMessageDialog(null, "Please enter same password.");
            return;
        }
        System.out.println(account.getArea());
    }//GEN-LAST:event_createJButtonActionPerformed

